
public class LoopLearning
{
    public static void main(String[] args){
        //1. initialization
        //2. test
        //3. change
        int i = 0;
        while (i<10){
        System.out.println(i);
        i++;
        }
        //for(initialization; test; change)
        for(int j=0;j<10;j++){
        System.out.println(j);
        }
    }
}
